        <div class="content">
          <p>Struktur kurikulum SMK/MAK terbagi menjadi 2 (dua), yaitu:</p>
          <ul>
            <li>
              <p>Pembelajaran intrakurikuler; dan</p>
            </li>
            <li>
              <p>
                Projek Penguatan Profil Pelajar Pancasila yang dialokasikan dari
                total JP mata pelajaran umum dan beberapa mata pelajaran pilihan
                per tahun.
              </p>
            </li>
          </ul>
          <p>
            Pembelajaran intrakuler di SMK/MAK pun terbagi menjadi 2 (dua),
            yaitu
            <strong>Kelompok Mata Pelajaran Umum</strong> dan
            <strong>Kejuruan</strong>.
          </p>
          <h4>Kelompok Umum</h4>
          <p>
            Kelompok mata pelajaran yang berfungsi membentuk murid menjadi
            pribadi yang utuh, sesuai fase perkembangannya. Murid diharapkan
            memiliki norma-norma kehidupan sebagai makhluk individu dan makhluk
            sosial, sebagai warga negara Indonesia dan warga dunia.
          </p>
          <p>Beberapa mata pelajaran yang termasuk dalam kelompok umum:</p>
          <ul>
            <li>
              <p>
                <strong>Projek IPAS</strong>. Mata pelajaran yang mengembangkan
                literasi sains dengan aspek-aspek ilmu pengetahuan alam dan
                sosial. Mata pelajaran ini disampaikan dalam tema-tema kehidupan
                yang kontekstual dan aktual.
              </p>
            </li>
            <li>
              <p>
                <strong>Bahasa Inggris</strong> dan <strong>Matematika</strong>.
                Di kelas 10, kedua mata pelajaran ini berisi materi umum dan
                dasar. Sementara di kelas 11 dan 12, fokus dua mata pelajaran
                ini adalah pendalamam materi secara kontekstual terhadap
                substansi kejuruan pada masing-masing Program Keahlian.
              </p>
            </li>
            <li>
              <p>
                <strong>Informatika</strong>. Mata pelajaran ini dirancang sama
                dengan satuan pendidikan lain tapi bisa disesuaikan dengan
                Program Keahlian peserta didik.
              </p>
            </li>
          </ul>
          <h4>Kelompok Kejuruan</h4>
          <p>
            Kelompok mata pelajaran yang berfungsi membentuk murid agar memiliki
            kompetensi sesuai perkembangan dunia kerja, serta ilmu pengetahuan,
            teknologi, seni dan budaya.
          </p>
          <p>Beberapa mata pelajaran yang ada di SMK/MAK:</p>
          <ul>
            <li>
              <p>
                <strong>Mata Pelajaran Kejuruan</strong>. Di kelas 10, Mata
                Pelajaran Kejuruan berpusat pada pelajaran dasar-dasar Program
                Keahlian. Di kelas 11 dan 12, mata pelajaran ini mencakup
                kelompok unit kompetensi yang dikembangkan secara lebih teknis
                sesuai Konsentrasi Keahlian yang dipilih.
              </p>
            </li>
            <li>
              <p>
                <strong>Mata Pelajaran Kreatif dan Kewirausahaan</strong>. Mata
                pelajaran ini menjadi alat bagi murid untuk mengaktualisisasikan
                dan mengekspresikan kompetensi yang dikuasai. Hal ini dilakukan
                melalui pembuatan produk atau pekerjaan layanan jasa secara
                kreatif dan bernilai ekonomis.
              </p>
            </li>
            <li>
              <p>
                <strong>Mata Pelajaran Pilihan</strong>. Mata pelajaran yang
                dipilih oleh murid sesuai dengan renjana (passion) untuk
                pengembangan diri, melanjutkan pendidikan, berwirausaha, maupun
                bekerja pada bidang yang dipilih. Murid dapat mendalami mata
                pelajaran kejuruan di konsentrasi keahliannya, mata pelajaran
                kejuruan lintas konsentrasi keahlian, mata pelajaran umum, atau
                mata pelajaran kelompok pilihan yang diajarkan di fase F SMA/MA.
              </p>
            </li>
          </ul>
          <h4>Pemilihan Konsentrasi Pada Satu Program Keahlian</h4>
          <p>
            Ada beberapa hal terkait pemilihan konsentrasi pada satu Program
            Keahlian yang perlu diperhatikan:
          </p>
          <ol>
            <li>
              <p>
                Pemilihan konsentrasi dilakukan berdasarkan kebutuhan tenaga
                kerja di dunia kerja yang menjadi sasaran murid.
              </p>
            </li>
            <li>
              <p>
                Satu program keahlian bisa mencakup satu atau lebih konsentrasi.
              </p>
            </li>
            <li>
              <p>
                Jika ada konsentrasi yang berbeda dalam satu program keahlian,
                maka akan diselenggarakan dalam rombongan belajar yang berbeda.
              </p>
            </li>
          </ol>

            Seluruh jurusan tersebut memiliki akreditasi B yang sudah ditetapkan
            dan terverifikasi oleh Badan Akreditasi Nasional Sekolah/Madrasah
            dengan surat keputusan nomor 69420/BAN-SM/SK/2017.
